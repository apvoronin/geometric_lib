def area(a, b):
    '''
    Принимает длину и ширину прямоугольника, возвращает его площадь
    Пример вызова:
    Входные данные: 7 8
    Выходные данные: 56
    '''
    return a * b 

def perimeter(a, b):
    '''
    Принимает длину и ширину прямоугольника, возвращает его периметр
    Пример вызова:
    Входные данные: 25 17
    Выходные данные: 42
    '''
    return 2*(a + b) 
