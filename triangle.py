def area(a, h):
    '''
    Принимает длины основания и высоты треугольника, возвращает его площадь
    Пример вызова:
    Входные данные: 11 7
    Выходные данные: 38,5
    '''
    return a * h / 2 

def perimeter(a, b, c):
    '''
    Принимает длины всех сторон треугольника, возвращает его периметр
    Пример вызова:
    Входные данные: 61,5 127,5 50
    Выходные данные: 239
    '''
    return a + b + c 
